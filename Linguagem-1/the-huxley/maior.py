'''
Descrição
Faça um programa que recebe dois números inteiros distintos e 
exibe o maior.

Formato de entrada

Um número inteiro na primeira linha; um número inteiro na 
segunda linha.

Formato de saída

O maior dos números.
'''

x = int(input())
y = int(input())
if x>y:
    print(x)
else:
    print(y)
    
