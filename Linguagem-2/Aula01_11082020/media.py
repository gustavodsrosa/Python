'''
Escreva um programa que solicita ao usuário que digite
três valores e exiba na tela a média dos valores
digitados.
'''

valor1 = int(input('Primeiro Número: '))
valor2 = int(input('Segundo Número: '))
valor3 = int(input('Terceiro Número: '))

media = (valor1 + valor2 + valor3) / 3

print('A média é de: ', media)